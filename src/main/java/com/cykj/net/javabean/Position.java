package com.cykj.net.javabean;


public class Position {

  private long poid;
  private String type;
  private long state;


  public long getPoid() {
    return poid;
  }

  public void setPoid(long poid) {
    this.poid = poid;
  }


  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }


  public long getState() {
    return state;
  }

  public void setState(long state) {
    this.state = state;
  }

}
